    int num;
    cout << "삭제하고자 하는 도형의 인덱스 >> ";  // 삭제할 도형의 인덱스를 입력받기 위한 메시지를 출력합니다.
    cin >> num;  // 사용자가 입력한 인덱스를 받습니다.
